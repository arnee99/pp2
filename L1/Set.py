# Python program to demonstrate  
# Creation of Set in Python 
    
# Creating a Set 
set1 = set() 
print("Initial blank Set: ") 
print(set1) 
    
# Creating a Set with  
# the use of a String 
set1 = set("GeeksForGeeks") 
print("\nSet with the use of String: ") 
print(set1) 
  
# Creating a Set with 
# the use of a List 
set1 = set(["Geeks", "For", "Geeks1"]) 
print("\nSet with the use of List: ") 
print(set1) 
